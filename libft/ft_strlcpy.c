#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	int		l;
	size_t	i;

	l = ft_strlen(src);
	if (size > 0)
	{
		i = 0;
		while (src[i] && i < size - 1)
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (l);
}
