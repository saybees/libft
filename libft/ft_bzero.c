#include "libft.h"

void    ft_bzero(void *s, size_t n)
{
    char    *str;
    size_t      i;

    str = (char *)s;
    i = 0;
    while (str[i] || i < n - 1)
    {
        str[i] = '\0';
        i++;
    }
}
